export function getClientLang() {
  try {
    const user = JSON.parse(sessionStorage.getItem('clientUser') || '{}')
    return user.lingua || 'it'
  } catch {
    return 'it'
  }
}

export function t(key) {
  const lang = getClientLang()
  return (translations[lang] && translations[lang][key]) || translations.it[key] || key
}

export function getDateLocale() {
  const lang = getClientLang()
  return lang === 'en' ? 'en-GB' : lang === 'fr' ? 'fr-FR' : 'it-IT'
}

